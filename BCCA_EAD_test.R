		<scopecontent>
			<p>The Bates College Clothing Archive (BCCA) is a research and study collection of historical clothing owned by Bates College in Lewiston, Maine. The collection spans from roughly 1840 to 1930. We hold a collection of clothing owned by the family of Harry W. Rowe, Bates College's first Dean of Faculty, as well as items belonging to Barbara Wiggin of Lewiston, Maine, all from 1910 to 1940. Our collection is strongest, however, in late nineteenth century middle-class women's clothing, such as day bodices, skirts, and outerwear.</p>
		</scopecontent>
		<bioghist>
			<p>Most items in this collection are of unknown provenance, and were donated to the Bates Department of Theater and Dance with the intention of being used as theatrical costumes. The collection was processed by Grace Acton during the 2023-2024, in partial fulfillment of an Honors Thesis in Interdisciplinary Studies.</p>
